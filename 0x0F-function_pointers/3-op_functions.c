#include "3-calc.h"

/**
 * op_add - add two integers
 *
 * @a: the first int
 * @b: second int
 *
 * Return: int
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts two numbers
 *
 * @a:int input
 * @b: int input
 *
 * Return: int
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two integers
 *
 * @a: int input
 * @b: int input
 *
 * Return: int
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two integers
 *
 * @a: int input
 * @b: int input
 *
 * Return: int
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module of two numbers
 *
 * @a: int input
 * @b: int input
 *
 * Return: int
*/

int op_mod(int a, int b)
{
	return (a % b);
}
